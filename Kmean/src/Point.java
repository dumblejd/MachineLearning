
public class Point 
{
	int number;
	double x;
	double y;
	public Point(int number, double x, double y) {
		super();
		this.number = number;
		this.x = x;
		this.y = y;
	}
	
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	
}
